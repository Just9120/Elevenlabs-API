def parse_keyterms(raw_text: str) -> list[str]:
    terms = []
    seen = set()

    for line in raw_text.splitlines():
        term = re.sub(r"\s+", " ", line).strip()
        if not term:
            continue

        if len(term) >= 50:
            raise ValueError(f"Keyterm слишком длинный (>= 50 символов): {term}")

        if len(term.split()) > 5:
            raise ValueError(f"Keyterm содержит больше 5 слов: {term}")

        norm = normalize_name_for_compare(term)
        if norm not in seen:
            seen.add(norm)
            terms.append(term)

    if len(terms) > 1000:
        raise ValueError("Количество keyterms не должно превышать 1000.")

    return terms


