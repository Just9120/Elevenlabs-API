def print_preflight_summary(run_ctx: RunExecutionContext, source_mode: str):
    render_colab_preflight_summary(build_preflight_summary(run_ctx=run_ctx, source_mode=source_mode))
def print_success_summary(successes: list[dict]):
    if not successes:
        return

    action_counts = {}
    for item in successes:
        action_counts[item["action"]] = action_counts.get(item["action"], 0) + 1

    print("\nУспешные действия:")
    for action, count in action_counts.items():
        print(f"- {action_label(action)}: {count}")

    print("\nДокументы:")
    for item in successes:
        line = f"- [{action_label(item['action'])}] {item['doc_name']}: {item['link']}"
        if item.get("archived_name"):
            line += f" | архив: {item['archived_name']}"
        print(line)


def resolve_run_status(success_count: int, error_count: int) -> str:
    if success_count > 0 and error_count == 0:
        return "success"
    if success_count > 0 and error_count > 0:
        return "partial_success"
    if success_count == 0 and error_count > 0:
        return "failed"
    return "no_files_processed"


def build_run_analytics_record(
    run_id: str,
    timer: RunTimer,
    source_mode: str,
    run_ctx: Optional[RunExecutionContext],
    successes: list[dict],
    errors: list[dict],
    skipped: list[dict],
) -> dict:
    extension_summary = {}
    for item in successes + errors + skipped:
        source_name = str(item.get("source") or item.get("source_name") or "").strip()
        ext = Path(source_name).suffix.lower()
        if ext:
            extension_summary[ext] = extension_summary.get(ext, 0) + 1
    return {
        "schema_version": 1,
        "event_type": "run_summary",
        "run_id": run_id,
        "created_at": utc_now_iso(),
        "source_mode": source_mode,
        "provider": run_ctx.options.provider if run_ctx else None,
        "provider_model": run_ctx.options.provider_model_id if run_ctx else None,
        "language_mode": run_ctx.options.language_mode if run_ctx else None,
        "language_label": run_ctx.options.language_label if run_ctx else None,
        "separate_speakers": run_ctx.options.separate_speakers if run_ctx else None,
        "extract_audio_from_video_enabled": run_ctx.options.extract_audio_from_video_enabled if run_ctx else None,
        "keyterms_count": len(run_ctx.options.keyterms) if run_ctx else None,
        "conflict_mode": run_ctx.conflict_mode if run_ctx else None,
        "ignore_manifest": run_ctx.ignore_manifest if run_ctx else None,
        "status": resolve_run_status(len(successes), len(errors)),
        "success_count": len(successes),
        "error_count": len(errors),
        "skipped_count": len(skipped),
        "file_count": len(successes) + len(errors) + len(skipped),
        "file_extensions": extension_summary,
        "timing_totals": dict(timer.totals),
        "timing_counts": dict(timer._counts),
        "timing_analysis": build_timing_analysis(timer),
    }


def on_start_clicked(_):
    with output_widget:
        clear_output()
        start_button.disabled = True
        check_source_button.disabled = True
        reset_progress()
        timer = RunTimer()
        timer.start("run_total")
        run_ctx: Optional[RunExecutionContext] = None
        mode = mode_widget.value
        successes, errors, skipped = [], [], []

        try:
            with timer.measure("startup_cleanup"):
                cleanup_stale_project_temp_files()

            if not folder_picker_state["selected_id"]:
                raise ValueError("Сначала выбери папку назначения в блоке выше и нажми 'Выбрать текущую папку'.")

            output_folder_id = folder_picker_state["selected_id"]
            output_folder_path = folder_picker_state["selected_path"]
            conflict_mode = conflict_mode_widget.value
            with timer.measure("runtime_context_build"):
                run_ctx = build_run_execution_context(
                    output_folder_id=output_folder_id,
                    output_folder_path=output_folder_path,
                    conflict_mode=conflict_mode,
                    use_manifest=use_manifest_widget.value,
                    options=collect_runtime_options_from_ui(),
                )

            print_preflight_summary(run_ctx=run_ctx, source_mode=mode)

            if mode == "local_file":
                print("Выбери один файл с компьютера...")
                with timer.measure("local_upload_wait"):
                    uploaded = files.upload()
                if len(uploaded) != 1:
                    raise ValueError("Нужно выбрать ровно один файл.")
                warn_about_large_local_uploads(uploaded)

                with timer.measure("source_processing_total"):
                    successes, errors, skipped = process_local_uploaded(
                        uploaded=uploaded,
                        run_ctx=run_ctx,
                        progress_callback=set_progress,
                        timer=timer,
                        source_mode=mode,
                    )

            elif mode == "local_multi":
                print("Выбери несколько файлов с компьютера...")
                with timer.measure("local_upload_wait"):
                    uploaded = files.upload()
                if len(uploaded) < 1:
                    raise ValueError("Нужно выбрать хотя бы один файл.")
                warn_about_large_local_uploads(uploaded)

                with timer.measure("source_processing_total"):
                    successes, errors, skipped = process_local_uploaded(
                    uploaded=uploaded,
                    run_ctx=run_ctx,
                    progress_callback=set_progress,
                    timer=timer,
                    source_mode=mode,
                    )

            elif mode == "drive_file":
                source_input = get_source_input_value()
                if not source_input:
                    raise ValueError("Укажи путь / ссылку или выбери файл в Google Drive.")

                with timer.measure("source_processing_total"):
                    successes, errors, skipped = process_drive_file_input(
                        source_input=source_input,
                        run_ctx=run_ctx,
                        progress_callback=set_progress,
                        timer=timer,
                    )

            elif mode == "drive_folder":
                source_input = get_source_input_value()
                if not source_input:
                    raise ValueError("Укажи путь / ссылку или выбери папку в Google Drive.")

                with timer.measure("source_collect"):
                    preview_info = inspect_source_input(
                        mode=mode,
                        source_input=source_input,
                        recursive=recursive_widget.value,
                        output_folder_id=run_ctx.output_folder_id,
                        conflict_mode=run_ctx.conflict_mode
                    )
                print(f"Найдено файлов перед запуском: {preview_info['count']}")
                print(f"Совпадений в папке назначения: {preview_info['conflict_count']}")
                if preview_info["conflict_count"]:
                    print(conflict_mode_result_hint(run_ctx.conflict_mode))

                with timer.measure("source_processing_total"):
                    successes, errors, skipped = process_drive_folder_input(
                        source_input=source_input,
                        recursive=recursive_widget.value,
                        run_ctx=run_ctx,
                        progress_callback=set_progress
                    )

            print("\n====================")
            print("ИТОГ")
            print("====================")
            print(f"Успешно: {len(successes)}")
            print(f"Пропущено: {len(skipped)}")
            print(f"С ошибкой: {len(errors)}")

            print_success_summary(successes)

            if skipped:
                print("\nПропуски:")
                for item in skipped:
                    print(f"- {item['source']}: {item['reason']}")

            if errors:
                print("\nОшибки:")
                for item in errors:
                    print(f"- {item['source']}: {item['error']}")

        except Exception as e:
            print(f"Ошибка: {e}")

        finally:
            timer.stop("run_total")
            print_timing_summary(timer)
            if run_ctx is not None:
                try:
                    run_id = str(uuid.uuid4())
                    analytics_record = build_run_analytics_record(
                        run_id=run_id,
                        timer=timer,
                        source_mode=mode,
                        run_ctx=run_ctx,
                        successes=successes,
                        errors=errors,
                        skipped=skipped,
                    )
                    json.dumps(analytics_record, ensure_ascii=False)
                    append_analytics_record(analytics_record)
                    print("Analytics log updated: VoiceOps Workspace/analytics/elevenlabs_transcription_runs.jsonl")
                except Exception:
                    print("Analytics log warning: failed to update analytics file (non-fatal).")
            start_button.disabled = False
            check_source_button.disabled = False
            if progress_bar.layout.visibility == "visible" and progress_bar.value < progress_bar.max:
                progress_bar.bar_style = "warning"

