def warn_about_large_local_uploads(uploaded: dict):
    large = []
    for filename, file_bytes in uploaded.items():
        size_mb = len(file_bytes) / (1024 * 1024)
        if size_mb >= LARGE_LOCAL_FILE_WARNING_MB:
            large.append((filename, size_mb))

    if large:
        print("Предупреждение: обнаружены крупные локальные файлы.")
        print("Для длинных лекций обычно стабильнее использовать режим Google Drive.")
        for filename, size_mb in large:
            print(f"- {filename}: {size_mb:.1f} MB")


