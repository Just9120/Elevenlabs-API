def transcribe_fileobj(
    filename: str,
    fileobj,
    mime_type: str,
    options: TranscriptionRuntimeOptions,
) -> str:
    url = "https://api.elevenlabs.io/v1/speech-to-text"

    headers = {
        "xi-api-key": ELEVENLABS_EFFECTIVE_API_KEY,
        "Accept": "application/json",
        "Connection": "close",
    }

    data = build_transcription_form_data(options)

    fileobj.seek(0)

    try:
        resp = requests.post(
            url,
            headers=headers,
            files={"file": (filename, fileobj, mime_type)},
            data=data,
            timeout=(60, 7200)
        )
        resp.raise_for_status()

    except requests.exceptions.SSLError as e:
        raise RuntimeError(
            "SSL/TLS соединение оборвалось во время загрузки файла в ElevenLabs. "
            "Обычно это временный сетевой сбой или слишком тяжёлый исходный файл."
        ) from e

    except requests.HTTPError:
        log_safe_http_error("ElevenLabs", resp)
        raise

    except requests.exceptions.RequestException as e:
        raise RuntimeError(f"Ошибка запроса к ElevenLabs: {e}") from e

    result = resp.json()
    transcript_text = build_speaker_labeled_transcript(result) if options.separate_speakers else (result.get("text") or "").strip()

    if not transcript_text:
        raise RuntimeError("ElevenLabs вернул пустой текст.")

    return transcript_text


def build_openai_diarized_transcript(result: dict) -> str:
    segments = result.get("segments") or []
    plain_text = (result.get("text") or "").strip()

    if not segments:
        return plain_text

    speaker_labels = {}
    speaker_blocks = []
    current_speaker = None
    current_parts = []

    def flush_block():
        nonlocal current_speaker, current_parts
        if current_speaker is None:
            return
        segment_text = format_speaker_segment_text(" ".join(current_parts))
        if not segment_text:
            current_parts = []
            return
        speaker_number = speaker_labels.setdefault(str(current_speaker), len(speaker_labels) + 1)
        speaker_blocks.append(f"Спикер {speaker_number}:\n{segment_text}")
        current_parts = []

    for segment in segments:
        segment_text = format_speaker_segment_text(segment.get("text", ""))
        if not segment_text:
            continue

        speaker = segment.get("speaker") or "unknown"
        if current_speaker is None:
            current_speaker = speaker

        if speaker != current_speaker:
            flush_block()
            current_speaker = speaker

        current_parts.append(segment_text)

    flush_block()

    if not speaker_blocks:
        return plain_text

    return "\n\n".join(speaker_blocks).strip()


