def build_speaker_labeled_transcript(result: dict) -> str:
    words = result.get("words") or []
    plain_text = (result.get("text") or "").strip()

    if not words:
        return plain_text

    speaker_labels = {}
    speaker_blocks = []
    current_speaker = None
    current_tokens = []

    def flush_block():
        nonlocal current_tokens, current_speaker
        if current_speaker is None:
            return
        segment_text = format_speaker_segment_text("".join(current_tokens))
        if not segment_text:
            current_tokens = []
            return
        speaker_number = speaker_labels.setdefault(current_speaker, len(speaker_labels) + 1)
        speaker_blocks.append(f"Спикер {speaker_number}:\n{segment_text}")
        current_tokens = []

    saw_any_speaker = False

    for item in words:
        token_text = item.get("text", "")
        speaker_id = item.get("speaker_id")

        if not token_text:
            continue

        if not speaker_id:
            current_tokens.append(token_text)
            continue

        saw_any_speaker = True

        if current_speaker is None:
            current_speaker = speaker_id

        if speaker_id != current_speaker:
            flush_block()
            current_speaker = speaker_id

        current_tokens.append(token_text)

    flush_block()

    if not saw_any_speaker or not speaker_blocks:
        return plain_text

    return "\n\n".join(speaker_blocks).strip()


