def split_openai_audio_if_needed(prepared_path: str, prepared_name: str) -> tuple[list[tuple[str, str]], list[str]]:
    file_size = os.path.getsize(prepared_path)
    if file_size <= OPENAI_UPLOAD_LIMIT_BYTES:
        return [(prepared_path, prepared_name)], []

    duration = get_media_duration_seconds(prepared_path)
    if duration <= 0:
        raise RuntimeError("Не удалось определить длительность файла для smart split под OpenAI.")

    safety_ratio = 0.92
    base_target_duration = max(
        float(OPENAI_MIN_SPLIT_DURATION_SECONDS),
        duration * (OPENAI_SEGMENT_TARGET_BYTES / file_size) * safety_ratio,
    )

    created_paths = []
    parts = []
    start_seconds = 0.0
    part_index = 1

    while start_seconds < duration - 0.01:
        remaining = duration - start_seconds
        target_duration = min(base_target_duration, remaining)
        part_duration = choose_openai_split_duration(
            input_path=prepared_path,
            start_seconds=start_seconds,
            target_duration=target_duration,
            total_duration=duration,
        )

        if part_duration <= 0:
            break

        while True:
            output_path = create_openai_audio_chunk(
                prepared_path=prepared_path,
                start_seconds=start_seconds,
                part_duration=part_duration,
            )

            part_size = os.path.getsize(output_path)
            if part_size <= OPENAI_UPLOAD_LIMIT_BYTES:
                created_paths.append(output_path)
                part_name = f"{Path(prepared_name).stem} - part {part_index:03d}.m4a"
                parts.append((output_path, part_name))

                if start_seconds + part_duration >= duration - 0.01:
                    start_seconds = duration
                else:
                    overlap_seconds = min(
                        OPENAI_SPLIT_OVERLAP_SECONDS,
                        max(0.0, part_duration / 4),
                    )
                    next_start = start_seconds + part_duration - overlap_seconds
                    if next_start <= start_seconds + 0.25:
                        next_start = start_seconds + part_duration
                    start_seconds = next_start

                part_index += 1
                break

            os.remove(output_path)
            if part_duration <= OPENAI_MIN_SPLIT_DURATION_SECONDS:
                raise RuntimeError(
                    "Даже после smart split часть файла всё ещё больше 25 MB. "
                    "Попробуй дополнительно ужать исходник или разделить запись заранее."
                )

            reduced_target_duration = max(
                float(OPENAI_MIN_SPLIT_DURATION_SECONDS),
                part_duration * 0.8,
            )
            next_part_duration = choose_openai_split_duration(
                input_path=prepared_path,
                start_seconds=start_seconds,
                target_duration=min(reduced_target_duration, remaining),
                total_duration=duration,
            )
            if next_part_duration >= part_duration - 0.25:
                next_part_duration = max(
                    float(OPENAI_MIN_SPLIT_DURATION_SECONDS),
                    part_duration * 0.8,
                )
            part_duration = min(next_part_duration, remaining)

    return parts, created_paths

def build_openai_transcription_form_data(options: TranscriptionRuntimeOptions) -> list[tuple[str, str]]:
    data = [
        ("model", options.provider_model_id),
    ]

    if options.separate_speakers:
        data.extend([
            ("response_format", "diarized_json"),
            ("chunking_strategy", "auto"),
        ])
    else:
        data.append(("response_format", "text"))
        prompt = build_openai_prompt_from_keyterms(options)
        if prompt:
            data.append(("prompt", prompt))

    if options.language_code:
        data.append(("language", options.language_code))

    return data


def transcribe_openai_fileobj(
    filename: str,
    fileobj,
    mime_type: str,
    options: TranscriptionRuntimeOptions,
) -> str:
    url = "https://api.openai.com/v1/audio/transcriptions"

    headers = {
        "Authorization": f"Bearer {OPENAI_API_KEY}",
    }

    data = build_openai_transcription_form_data(options)
    fileobj.seek(0)

    try:
        resp = requests.post(
            url,
            headers=headers,
            files={"file": (filename, fileobj, mime_type)},
            data=data,
            timeout=(60, 7200),
        )
        resp.raise_for_status()
    except requests.HTTPError:
        log_safe_http_error("OpenAI STT", resp)
        raise
    except requests.exceptions.RequestException as e:
        raise RuntimeError(f"Ошибка запроса к OpenAI STT: {e}") from e

    if options.separate_speakers:
        result = resp.json()
        transcript_text = build_openai_diarized_transcript(result)
    else:
        transcript_text = (resp.text or "").strip()

    if not transcript_text:
        raise RuntimeError("OpenAI вернул пустой текст.")

    return transcript_text


def transcribe_media_path_elevenlabs(
    input_path: str,
    original_filename: str,
    options: TranscriptionRuntimeOptions,
) -> str:
    cleanup_paths = []
    send_path = input_path
    send_name = original_filename

    try:
        if options.extract_audio_from_video_enabled and should_convert_to_m4a(input_path, original_filename):
            converted_path, converted_name = convert_media_to_m4a(input_path, original_filename)
            cleanup_paths.append(converted_path)
            send_path = converted_path
            send_name = converted_name

        mime_type = mimetypes.guess_type(send_name)[0] or "application/octet-stream"
        with open(send_path, "rb") as f:
            return transcribe_fileobj(send_name, f, mime_type, options)

    finally:
        for path in cleanup_paths:
            if path and os.path.exists(path):
                os.remove(path)


def transcribe_media_path_openai(
    input_path: str,
    original_filename: str,
    options: TranscriptionRuntimeOptions,
    timer: Optional[RunTimer] = None,
) -> str:
    cleanup_paths = []

    try:
        with timer.measure("media_prepare") if timer else nullcontext():
            prepared_path, prepared_name = convert_media_to_openai_m4a(input_path, original_filename)
        cleanup_paths.append(prepared_path)

        with timer.measure("postprocess_merge") if timer else nullcontext():
            parts, part_cleanup_paths = split_openai_audio_if_needed(prepared_path, prepared_name)
        cleanup_paths.extend(part_cleanup_paths)

        if len(parts) > 1:
            print(f"OpenAI smart split: {len(parts)} частей")
            print("OpenAI merge: overlap-aware, split старается попадать в паузы")
            if options.separate_speakers:
                print_openai_diarize_split_warning()

        transcripts = []
        for idx, (part_path, part_name) in enumerate(parts, start=1):
            if len(parts) > 1:
                print(f"  → OpenAI часть {idx}/{len(parts)}: {part_name}")
            mime_type = mimetypes.guess_type(part_name)[0] or "audio/mp4"
            with open(part_path, "rb") as f:
                transcripts.append(transcribe_openai_fileobj(part_name, f, mime_type, options))

        transcript_text = merge_transcript_parts_with_overlap(transcripts)
        if not transcript_text:
            raise RuntimeError("OpenAI не вернул итоговый текст после обработки частей.")
        return transcript_text

    finally:
        for path in cleanup_paths:
            if path and os.path.exists(path):
                os.remove(path)


