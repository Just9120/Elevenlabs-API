def list_source_children(parent_id: str, include_files: bool) -> list[dict]:
    all_items = []
    page_token = None

    while True:
        result = drive_service.files().list(
            q=f"'{parent_id}' in parents and trashed = false",
            spaces="drive",
            fields="nextPageToken, files(id, name, mimeType, webViewLink, shortcutDetails, modifiedTime, size)",
            orderBy="folder,name_natural",
            pageSize=1000,
            pageToken=page_token,
            supportsAllDrives=True,
            includeItemsFromAllDrives=True,
        ).execute()

        all_items.extend(result.get("files", []))
        page_token = result.get("nextPageToken")
        if not page_token:
            break

    resolved = []
    for item in all_items:
        if item.get("mimeType") == SHORTCUT_MIME:
            target_id = (item.get("shortcutDetails") or {}).get("targetId")
            if not target_id:
                continue
            try:
                item = get_drive_item_metadata(target_id)
            except Exception:
                continue

        if item.get("mimeType") == FOLDER_MIME:
            resolved.append(item)
            continue

        if include_files and is_supported_filename(item.get("name", "")):
            resolved.append(item)

    folders = [x for x in resolved if x.get("mimeType") == FOLDER_MIME]
    files_list = [x for x in resolved if x.get("mimeType") != FOLDER_MIME]
    folders.sort(key=lambda x: x["name"].lower())
    files_list.sort(key=lambda x: x["name"].lower())
    return folders + files_list


def build_source_item_label(item: dict) -> str:
    if item.get("mimeType") == FOLDER_MIME:
        return f"📁 {item['name']}"
    if is_video_filename(item.get("name", "")):
        return f"🎬 {item['name']}"
    return f"🎵 {item['name']}"


