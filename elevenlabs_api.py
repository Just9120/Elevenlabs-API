def file_exists_in_folder(folder_id: str, file_name: str, mime_type: Optional[str] = None) -> bool:
    safe_name = escape_drive_query_value(file_name)
    query = [
        f"'{folder_id}' in parents",
        f"name = '{safe_name}'",
        "trashed = false",
    ]
    if mime_type:
        query.append(f"mimeType = '{mime_type}'")

    result = drive_service.files().list(
        q=" and ".join(query),
        spaces="drive",
        fields="files(id, name)",
        pageSize=1,
        supportsAllDrives=True,
        includeItemsFromAllDrives=True,
    ).execute()

    return len(result.get("files", [])) > 0


def get_unique_name(folder_id: str, desired_name: str, mime_type: Optional[str] = None) -> str:
    root, ext = os.path.splitext(desired_name)

    if not file_exists_in_folder(folder_id, desired_name, mime_type):
        return desired_name

    counter = 2
    while True:
        candidate = f"{root} ({counter}){ext}"
        if not file_exists_in_folder(folder_id, candidate, mime_type):
            return candidate
        counter += 1


def build_existing_docs_index(folder_id: str) -> dict[str, list[dict]]:
    index = {}
    page_token = None

    while True:
        result = drive_service.files().list(
            q=(
                f"'{folder_id}' in parents and "
                f"mimeType = '{DOC_MIME}' and "
                f"trashed = false"
            ),
            spaces="drive",
            fields="nextPageToken, files(id, name, webViewLink, modifiedTime)",
            pageSize=1000,
            pageToken=page_token,
            supportsAllDrives=True,
            includeItemsFromAllDrives=True,
        ).execute()

        for item in result.get("files", []):
            norm = normalize_name_for_compare(item["name"])
            index.setdefault(norm, []).append(item)

        page_token = result.get("nextPageToken")
        if not page_token:
            break

    for norm_name in index:
        index[norm_name].sort(key=lambda x: x.get("modifiedTime", ""), reverse=True)

