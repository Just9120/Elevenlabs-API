    if isinstance(latest_manifest, dict):
        merged.update({k: v for k, v in latest_manifest.items() if k != "entries"})
        merged["entries"] = dict(latest_manifest.get("entries") or {})

    if isinstance(incoming_manifest, dict):
        merged.update({k: v for k, v in incoming_manifest.items() if k != "entries"})
        merged.setdefault("entries", {})
        merged["entries"].update(incoming_manifest.get("entries") or {})

    merged.setdefault("version", 1)
    merged.setdefault("entries", {})
    return merged


