def upsert_transcript_document(
    base_name: str,
    transcript_text: str,
    output_folder_id: str,
    docs_index: dict[str, list[dict]],
    conflict_mode: str,
    source_name: str,
    source_mode: str,
    options: TranscriptionRuntimeOptions,
) -> dict:
    existing = get_single_existing_doc_match(base_name, docs_index)

    def structured_text_for_doc(doc_title: str) -> str:
        language = options.language_label
        if options.language_code:
            language = f"{language} ({options.language_code})"
        metadata_lines = build_transcript_metadata_lines(
            source_name=source_name,
            source_mode=get_source_mode_label(source_mode),
            provider=options.provider_label,
            provider_model=options.provider_model_label,
            language=language,
            speakers_enabled=options.separate_speakers,
            created_at=utc_now_iso(),
        )
        return build_structured_transcript_document_text(
            document_title=doc_title,
            transcript_text=transcript_text,
            metadata_lines=metadata_lines,
        )

    if not existing:
        doc_name = get_unique_name(output_folder_id, base_name, DOC_MIME)
        doc = create_google_doc(doc_name, output_folder_id)
        write_title_and_text_to_doc(
            document_id=doc["id"],
            title=doc_name,
            transcript_text=structured_text_for_doc(doc_name),
            clear_first=False
        )
        add_doc_record_to_index(doc, docs_index)
        return {
            "status": "success",
            "action": "created",
            "doc_name": doc["name"],
            "link": doc["webViewLink"],
        }

    if conflict_mode == "skip":
        return {
            "status": "skipped",
            "reason": "Документ уже существует",
            "doc_name": existing["name"],
            "link": existing.get("webViewLink", ""),
        }

    if conflict_mode == "update":
        write_title_and_text_to_doc(
            document_id=existing["id"],
            title=existing["name"],
            transcript_text=structured_text_for_doc(existing["name"]),
            clear_first=True
        )
        return {
            "status": "success",
            "action": "updated",
            "doc_name": existing["name"],
            "link": existing.get("webViewLink", ""),
        }

    if conflict_mode == "archive_recreate":
        archived = archive_existing_doc(existing, output_folder_id, docs_index)
        new_name = get_unique_name(output_folder_id, base_name, DOC_MIME)
        doc = create_google_doc(new_name, output_folder_id)
        write_title_and_text_to_doc(
            document_id=doc["id"],
            title=doc["name"],
            transcript_text=structured_text_for_doc(doc["name"]),
            clear_first=False
        )
        add_doc_record_to_index(doc, docs_index)
        return {
            "status": "success",
            "action": "archived_and_recreated",
            "doc_name": doc["name"],
            "link": doc["webViewLink"],
            "archived_name": archived["name"],
        }

    if conflict_mode == "copy_suffix":
        doc_name = get_unique_name(output_folder_id, base_name, DOC_MIME)
        doc = create_google_doc(doc_name, output_folder_id)
        write_title_and_text_to_doc(
            document_id=doc["id"],
            title=doc["name"],
            transcript_text=structured_text_for_doc(doc["name"]),
            clear_first=False
        )
        add_doc_record_to_index(doc, docs_index)
        return {
            "status": "success",
            "action": "created_copy",
            "doc_name": doc["name"],
            "link": doc["webViewLink"],
        }

    raise ValueError(f"Неизвестный режим конфликта: {conflict_mode}")


