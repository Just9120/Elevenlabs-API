def should_skip_by_manifest(manifest: dict, source_signature: str, settings_signature: str) -> tuple[bool, str]:
    entry = get_manifest_entry(manifest, source_signature)
    if not entry:
        return False, ""

    status = entry.get("status")

    if status == "imported_done":
        return True, "Уже импортировано из существующего транскрипта"

    if status == "done" and entry.get("settings_signature") == settings_signature:
        return True, "Уже транскрибировано ранее с теми же настройками"

    if status == "in_progress":
        started_at = parse_iso_datetime(entry.get("started_at") or entry.get("updated_at"))
        if started_at and datetime.now(timezone.utc) - started_at <= timedelta(hours=MANIFEST_IN_PROGRESS_TTL_HOURS):
            return True, "Файл уже обрабатывается или недавно был запущен"

    return False, ""


def collect_import_candidates(mode: str, source_input: str, recursive: bool) -> list[dict]:
    resolved = resolve_drive_source_input(source_input)

    if mode == "drive_file":
        if resolved["source_type"] == "local_path":
            if resolved["kind"] != "file":
                raise ValueError("Указан путь к папке. Для импорта нужен файл.")
            filename = resolved["name"]
            if not is_supported_filename(filename):
                raise ValueError(f"Неподдерживаемое расширение: {Path(filename).suffix}")
            return [{
                "source_name": filename,
                "base_name": os.path.splitext(filename)[0],
                "source_signature": build_local_path_source_signature(resolved["path"], filename),
                "source_meta": {"type": "local_path", "path": resolved["path"]},
            }]

        if resolved["mimeType"] == FOLDER_MIME:
            raise ValueError("Указана ссылка на папку. Для импорта нужен файл.")
        filename = resolved["name"]
        if not is_supported_filename(filename):
            raise ValueError(f"Неподдерживаемое расширение: {Path(filename).suffix}")
        return [{
            "source_name": filename,
            "base_name": os.path.splitext(filename)[0],
            "source_signature": build_drive_source_signature(
                resolved["id"], filename, resolved.get("modifiedTime"), resolved.get("size")
            ),
            "source_meta": {
                "type": "drive",
                "file_id": resolved["id"],
                "modifiedTime": resolved.get("modifiedTime", ""),
                "size": str(resolved.get("size", "")),
            },
        }]

    if mode == "drive_folder":
        if resolved["source_type"] == "local_path":
            if resolved["kind"] != "folder":
                raise ValueError("Указан путь к файлу. Для импорта нужна папка.")
            file_list = collect_files_from_drive_folder_path(resolved["path"], recursive=recursive)
            return [{
                "source_name": os.path.basename(file_path),
                "base_name": os.path.splitext(os.path.basename(file_path))[0],
                "source_signature": build_local_path_source_signature(file_path, os.path.basename(file_path)),
                "source_meta": {"type": "local_path", "path": file_path},
            } for file_path in file_list]

        if resolved["mimeType"] != FOLDER_MIME:
            raise ValueError("Указана ссылка на файл. Для импорта нужна папка.")

        folder_files = collect_files_from_drive_folder_id(resolved["id"], recursive=recursive)
        return [{
            "source_name": item["display_name"],
            "base_name": os.path.splitext(item["name"])[0],
            "source_signature": build_drive_source_signature(item["id"], item["name"], item.get("modifiedTime"), item.get("size")),
            "source_meta": {
                "type": "drive",
                "file_id": item["id"],
                "modifiedTime": item.get("modifiedTime", ""),
                "size": str(item.get("size", "")),
            },
        } for item in folder_files]

    raise ValueError("Импорт существующих транскриптов доступен только для режимов Google Drive.")


IMPORT_PREFIX_PATTERNS = [
    r"^(лекция|урок|тема)\s*\d+[\s.)-:]*",
    r"^практика\s*№?\s*\d+[\s.)-:]*",
    r"^вебинар\s*[\s.)-:]*",
]


def normalize_for_import_match(name: str) -> str:
    value = os.path.splitext(name)[0]
    value = value.casefold().replace("ё", "е")
    value = value.replace("«", "").replace("»", "")
    value = value.replace('"', "").replace("'", "")
    value = value.replace("—", "-").replace("–", "-")
    value = re.sub(r"\s+", " ", value).strip()
    value = re.sub(r"\s*[-–—]\s*", " - ", value)
    value = re.sub(r"\s+", " ", value).strip(" .-_")
    return value


def strip_import_prefix(name: str) -> str:
    value = normalize_for_import_match(name)
    previous = None
    while value != previous:
        previous = value
        for pattern in IMPORT_PREFIX_PATTERNS:
            value = re.sub(pattern, "", value, count=1, flags=re.IGNORECASE).strip(" .-_")
    return value


def build_import_doc_indexes(output_folder_id: str) -> tuple[dict[str, list[dict]], dict[str, list[dict]]]:
    docs_index = build_existing_docs_index(output_folder_id)
    exact_index: dict[str, list[dict]] = {}
    stripped_index: dict[str, list[dict]] = {}

    for items in docs_index.values():
        for doc in items:
            exact_key = normalize_for_import_match(doc["name"])
            stripped_key = strip_import_prefix(doc["name"])
            exact_index.setdefault(exact_key, []).append(doc)
            stripped_index.setdefault(stripped_key, []).append(doc)

    return exact_index, stripped_index


def find_import_doc_match(base_name: str, exact_index: dict[str, list[dict]], stripped_index: dict[str, list[dict]]) -> tuple[Optional[dict], str, str]:
    exact_key = normalize_for_import_match(base_name)
    exact_matches = exact_index.get(exact_key, [])
    if len(exact_matches) == 1:
        return exact_matches[0], "exact_name", ""
    if len(exact_matches) > 1:
        return None, "ambiguous", "Найдено несколько Google Docs по точному совпадению имени"

    stripped_key = strip_import_prefix(base_name)
    stripped_matches = stripped_index.get(stripped_key, [])
    if len(stripped_matches) == 1:
        return stripped_matches[0], "stripped_prefix_name", ""
    if len(stripped_matches) > 1:
        return None, "ambiguous", "Найдено несколько Google Docs после снятия учебного префикса"

    return None, "not_found", "Совпадающий Google Doc не найден"


def import_existing_transcripts_by_name(
    mode: str,
    source_input: str,
    recursive: bool,
    output_folder_id: str,
    manifest: dict
) -> tuple[list[dict], list[dict], list[dict]]:
    exact_index, stripped_index = build_import_doc_indexes(output_folder_id)
    candidates = collect_import_candidates(mode, source_input, recursive)

    imported = []
    skipped = []
    ambiguous = []

    for item in candidates:
        doc, match_method, reason = find_import_doc_match(item["base_name"], exact_index, stripped_index)
        if not doc:
            target = ambiguous if match_method == "ambiguous" else skipped
            target.append({
                "source": item["source_name"],
                "reason": reason,
            })
            continue

        set_manifest_entry(
            manifest=manifest,
            source_signature=item["source_signature"],
            settings_signature="imported_by_name",
            status="imported_done",
            source_name=item["source_name"],
            doc_name=doc["name"],
            doc_link=doc.get("webViewLink", ""),
            note=f"Импортировано из существующего транскрипта ({match_method})",
            source_meta=item.get("source_meta", {}),
        )
        imported.append({
            "source": item["source_name"],
            "doc_name": doc["name"],
            "link": doc.get("webViewLink", ""),
            "match_method": match_method,
        })

    save_manifest(manifest)
    return imported, skipped, ambiguous


def check_or_standardize_existing_google_docs(
    mode: str,
    source_input: str,
    recursive: bool,
    output_folder_id: str,
    dry_run: bool = True,
) -> dict:
    exact_index, stripped_index = build_import_doc_indexes(output_folder_id)
    candidates = collect_import_candidates(mode, source_input, recursive)
    source_mode_label = get_source_mode_label(mode)
    report = {
        "dry_run": dry_run,
        "scanned": len(candidates),
        "missing_google_doc": [],
        "ambiguous": [],
        "already_structured": [],
        "would_standardize": [],
        "standardized": [],
        "errors": [],
    }

    for item in candidates:
        doc, match_method, reason = find_import_doc_match(item["base_name"], exact_index, stripped_index)
        if not doc:
            target = "ambiguous" if match_method == "ambiguous" else "missing_google_doc"
            report[target].append({
                "source": item["source_name"],
                "reason": reason,
            })
            continue

        try:
            existing_text = extract_google_doc_plain_text(doc["id"])
            result_item = {
                "source": item["source_name"],
                "doc_name": doc["name"],
                "link": doc.get("webViewLink", ""),
                "match_method": match_method,
            }
            if is_structured_transcript_document_text(existing_text):
                report["already_structured"].append(result_item)
                continue

            if dry_run:
                report["would_standardize"].append(result_item)
                continue

            structured_text = build_backfilled_transcript_document_text(
                document_title=doc["name"],
                source_name=item["source_name"],
                source_mode=source_mode_label,
                existing_document_text=existing_text,
                created_at=utc_now_iso(),
            )
            write_title_and_text_to_doc(
                document_id=doc["id"],
                title=doc["name"],
                transcript_text=structured_text,
                clear_first=True,
            )
            report["standardized"].append(result_item)
        except Exception as exc:
            report["errors"].append({
                "source": item["source_name"],
                "doc_name": doc.get("name", ""),
                "reason": str(exc),
            })

    return report


