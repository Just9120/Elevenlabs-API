def normalize_text_for_overlap_match(text: str) -> str:
    return re.sub(r"\s+", " ", text).strip().casefold()


def trim_duplicate_prefix_by_token_overlap(previous_text: str, current_text: str) -> str:
    previous_tokens = re.findall(r"\S+\s*", previous_text or "")
    current_tokens = re.findall(r"\S+\s*", current_text or "")

    max_overlap = min(
        OPENAI_MERGE_MAX_OVERLAP_TOKENS,
        len(previous_tokens),
        len(current_tokens),
    )

    for token_count in range(max_overlap, OPENAI_MERGE_MIN_OVERLAP_TOKENS - 1, -1):
        previous_window = "".join(previous_tokens[-token_count:])
        current_window = "".join(current_tokens[:token_count])

        normalized_previous = normalize_text_for_overlap_match(previous_window)
        normalized_current = normalize_text_for_overlap_match(current_window)

        if len(normalized_previous) < 30:
            continue

        if normalized_previous == normalized_current:
            return "".join(current_tokens[token_count:]).lstrip()

    return current_text


def merge_transcript_parts_with_overlap(transcripts: list[str]) -> str:
    clean_parts = [item.strip() for item in transcripts if item and item.strip()]
    if not clean_parts:
        return ""

    merged_parts = [clean_parts[0]]
    previous_raw_part = clean_parts[0]

    for current_part in clean_parts[1:]:
        trimmed_part = trim_duplicate_prefix_by_token_overlap(previous_raw_part, current_part)
        if trimmed_part.strip():
            merged_parts.append(trimmed_part.strip())
        previous_raw_part = current_part

    return "\n\n".join(merged_parts).strip()


def find_nearby_silence_split_point(input_path: str, start_seconds: float, target_end_seconds: float, total_duration: float) -> Optional[float]:
    ensure_ffprobe_available()

    window_start = max(
        start_seconds + 5.0,
        target_end_seconds - OPENAI_SILENCE_SEARCH_BEFORE_SECONDS,
    )
    window_end = min(
        total_duration,
        target_end_seconds + OPENAI_SILENCE_SEARCH_AFTER_SECONDS,
    )

    if window_end - window_start < 3.0:
        return None

    cmd = [
        "ffmpeg",
        "-hide_banner",
        "-nostats",
        "-ss", f"{window_start:.3f}",
        "-t", f"{window_end - window_start:.3f}",
        "-i", input_path,
        "-af", f"silencedetect=noise={OPENAI_SILENCE_NOISE_LEVEL}:d={OPENAI_SILENCE_MIN_DURATION_SECONDS}",
        "-f", "null",
        "-",
    ]

    try:
        result = subprocess.run(
            cmd,
            check=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
            text=True,
        )
    except subprocess.CalledProcessError:
        return None

    candidates = []
    for match in re.finditer(r"silence_end:\s*([0-9.]+)", result.stderr or ""):
        try:
            absolute_end = window_start + float(match.group(1))
        except Exception:
            continue

        if absolute_end <= start_seconds + OPENAI_MIN_SPLIT_DURATION_SECONDS:
            continue
        if absolute_end >= total_duration:
            continue
        candidates.append(absolute_end)

    if not candidates:
        return None

    candidates.sort(key=lambda value: (abs(value - target_end_seconds), value > target_end_seconds, value))
    return candidates[0]


def choose_openai_split_duration(input_path: str, start_seconds: float, target_duration: float, total_duration: float) -> float:
    remaining = max(0.0, total_duration - start_seconds)
    if remaining <= 0:
        return 0.0

    if remaining <= target_duration:
        return remaining

    target_end_seconds = min(total_duration, start_seconds + target_duration)
    silence_split_point = find_nearby_silence_split_point(
        input_path=input_path,
        start_seconds=start_seconds,
        target_end_seconds=target_end_seconds,
        total_duration=total_duration,
    )
    if silence_split_point is None:
        return min(target_duration, remaining)

    chosen_duration = silence_split_point - start_seconds
    if chosen_duration < OPENAI_MIN_SPLIT_DURATION_SECONDS:
        return min(target_duration, remaining)

    return min(chosen_duration, remaining)


def create_openai_audio_chunk(prepared_path: str, start_seconds: float, part_duration: float) -> str:
    ensure_ffmpeg_available()

    output_path = create_project_temp_file(suffix=".m4a")

    cmd = [
        "ffmpeg",
        "-y",
        "-ss", f"{start_seconds:.3f}",
        "-t", f"{part_duration:.3f}",
        "-i", prepared_path,
        "-vn",
        "-ac", str(OPENAI_PREP_AUDIO_CHANNELS),
        "-c:a", "aac",
        "-b:a", OPENAI_PREP_AUDIO_BITRATE,
        output_path,
    ]

    try:
        subprocess.run(
            cmd,
            check=True,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
        )
    except subprocess.CalledProcessError as e:
        stderr = e.stderr.decode("utf-8", errors="ignore")
        if os.path.exists(output_path):
            os.remove(output_path)
        raise RuntimeError(
            f"Не удалось разрезать аудио для OpenAI.\n{stderr[-1200:]}"
        ) from e

    return output_path


