def list_child_folders(parent_id: str) -> list[dict]:
    all_folders = []
    page_token = None

    while True:
        result = drive_service.files().list(
            q=(
                f"'{parent_id}' in parents and "
                f"mimeType = '{FOLDER_MIME}' and "
                f"trashed = false"
            ),
            spaces="drive",
            fields="nextPageToken, files(id, name)",
            orderBy="name_natural",
            pageSize=1000,
            pageToken=page_token,
            supportsAllDrives=True,
            includeItemsFromAllDrives=True,
        ).execute()

        all_folders.extend(result.get("files", []))
        page_token = result.get("nextPageToken")
        if not page_token:
            break

    all_folders.sort(key=lambda x: x["name"].lower())
    return all_folders


def folder_exists_in_parent(parent_id: str, folder_name: str) -> bool:
    safe_name = escape_drive_query_value(folder_name)
    query = (
        f"'{parent_id}' in parents and "
        f"mimeType = '{FOLDER_MIME}' and "
        f"name = '{safe_name}' and "
        f"trashed = false"
    )

    result = drive_service.files().list(
        q=query,
        spaces="drive",
        fields="files(id)",
        pageSize=1,
        supportsAllDrives=True,
        includeItemsFromAllDrives=True,
    ).execute()

    return len(result.get("files", [])) > 0


def create_folder_in_parent(parent_id: str, folder_name: str) -> dict:
    metadata = {
        "name": folder_name,
        "mimeType": FOLDER_MIME,
        "parents": [parent_id]
    }

    created = drive_service.files().create(
        body=metadata,
        fields="id, name",
        supportsAllDrives=True
    ).execute()

    return created


