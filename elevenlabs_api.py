def list_drive_folder_children(parent_id: str) -> list[dict]:
    all_items = []
    page_token = None

    while True:
        result = drive_service.files().list(
            q=f"'{parent_id}' in parents and trashed = false",
            spaces="drive",
            fields="nextPageToken, files(id, name, mimeType, shortcutDetails, modifiedTime, size)",
            orderBy="name_natural",
            pageSize=1000,
            pageToken=page_token,
            supportsAllDrives=True,
            includeItemsFromAllDrives=True,
        ).execute()

        items = result.get("files", [])
        all_items.extend(items)
        page_token = result.get("nextPageToken")

        if not page_token:
            break

    resolved = []
    for item in all_items:
        if item.get("mimeType") == SHORTCUT_MIME:
            target_id = (item.get("shortcutDetails") or {}).get("targetId")
            if not target_id:
                continue
            try:
                target_meta = get_drive_item_metadata(target_id)
                resolved.append(target_meta)
            except Exception:
                continue
        else:
            resolved.append(item)

    resolved.sort(key=lambda x: x["name"].lower())
    return resolved


def collect_files_from_drive_folder_path(folder_path: str, recursive: bool = False) -> list[str]:
    folder = Path(folder_path)

    if not folder.exists():
        raise FileNotFoundError(f"Папка не найдена: {folder_path}")
    if not folder.is_dir():
        raise NotADirectoryError(f"Это не папка: {folder_path}")

    if recursive:
        candidates = [p for p in folder.rglob("*") if p.is_file()]
    else:
        candidates = [p for p in folder.iterdir() if p.is_file()]

    result = []
    for p in candidates:
        if p.suffix.lower() in SUPPORTED_EXTENSIONS:
            result.append(str(p))

    result.sort()
    return result


def collect_files_from_drive_folder_id(folder_id: str, recursive: bool = False, prefix: str = "") -> list[dict]:
    result = []
    items = list_drive_folder_children(folder_id)

    for item in items:
        item_id = item["id"]
        item_name = item["name"]
        item_mime = item["mimeType"]

        display_name = f"{prefix}/{item_name}" if prefix else item_name

        if item_mime == FOLDER_MIME:
            if recursive:
                result.extend(
                    collect_files_from_drive_folder_id(
                        item_id,
                        recursive=True,
                        prefix=display_name
                    )
                )
            continue

        if item_mime.startswith("application/vnd.google-apps"):
            continue

        if is_supported_filename(item_name):
            result.append({
                "id": item_id,
                "name": item_name,
                "display_name": display_name,
                "modifiedTime": item.get("modifiedTime", ""),
                "size": item.get("size", ""),
            })

    result.sort(key=lambda x: x["display_name"].lower())
    return result


def resolve_drive_source_input(raw_value: str) -> dict:
    value = raw_value.strip()
    if not value:
        raise ValueError("Поле пути / ссылки пустое.")

    if looks_like_google_drive_url(value):
        item_id = extract_drive_id_from_url(value)
        if not item_id:
            raise ValueError("Не удалось извлечь ID из ссылки Google Drive.")

        meta = get_drive_item_metadata(item_id)
        return {
            "source_type": "drive_link",
            "id": meta["id"],
            "name": meta["name"],
            "mimeType": meta["mimeType"],
            "webViewLink": meta.get("webViewLink", ""),
            "modifiedTime": meta.get("modifiedTime", ""),
            "size": meta.get("size", ""),
        }

    ensure_drive_mounted()
    local_path = normalize_local_drive_path(value)

    if not os.path.exists(local_path):
        raise FileNotFoundError(
            "Путь не найден. Укажи корректный путь в /content/drive/... "
            "или вставь ссылку Google Drive."
        )

    if os.path.isfile(local_path):
        return {
            "source_type": "local_path",
            "kind": "file",
            "path": local_path,
            "name": os.path.basename(local_path),
        }

    if os.path.isdir(local_path):
        return {
            "source_type": "local_path",
            "kind": "folder",
            "path": local_path,
            "name": os.path.basename(local_path.rstrip("/")) or "MyDrive",
        }

    raise ValueError("Не удалось распознать источник.")


