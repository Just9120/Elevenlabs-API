def write_title_and_text_to_doc(document_id: str, title: str, transcript_text: str, clear_first: bool = False):
    prefix = f"{title}\n\n"
    document_text = transcript_text if transcript_text.startswith(prefix) else f"{prefix}{transcript_text}"
    requests_payload = []

    if clear_first:
        end_index = get_document_end_index(document_id)
        if end_index > 2:
            requests_payload.append({
                "deleteContentRange": {
                    "range": {
                        "startIndex": 1,
                        "endIndex": end_index - 1
                    }
                }
            })

    requests_payload.extend([
        {
            "insertText": {
                "location": {"index": 1},
                "text": prefix
            }
        },
        {
            "updateParagraphStyle": {
                "range": {
                    "startIndex": 1,
                    "endIndex": 1 + docs_text_len(title) + 1
                },
                "paragraphStyle": {
                    "namedStyleType": "TITLE"
                },
                "fields": "namedStyleType"
            }
        }
    ])

    # Docs insertText is not fully idempotent. Retrying ambiguous 5xx can duplicate
    # text if server applied the insert but client saw an error; keep retries narrow.
    execute_google_request_with_retry(
        docs_service.documents().batchUpdate(
            documentId=document_id,
            body={"requests": requests_payload}
        ),
        operation_label="docs_batch_update",
        retryable_status_codes=GOOGLE_DOCS_INSERT_RETRYABLE_STATUS_CODES,
    )

    insert_index = 1 + docs_text_len(prefix)
    body_text = document_text[len(prefix):]
    chunks = chunk_text_for_docs(body_text, DOC_INSERT_CHUNK_SIZE)

    for chunk in chunks:
        if not chunk:
            continue

        execute_google_request_with_retry(
            docs_service.documents().batchUpdate(
                documentId=document_id,
                body={
                    "requests": [
                        {
                            "insertText": {
                                "location": {"index": insert_index},
                                "text": chunk
                            }
                        }
                    ]
                }
            ),
            operation_label="docs_batch_update",
            retryable_status_codes=GOOGLE_DOCS_INSERT_RETRYABLE_STATUS_CODES,
        )

        insert_index += docs_text_len(chunk)


