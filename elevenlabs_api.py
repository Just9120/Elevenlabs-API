def chunk_text_for_docs(text: str, max_chars: int = DOC_INSERT_CHUNK_SIZE) -> list[str]:
    text = text.replace("\r\n", "\n").replace("\r", "\n")

    if not text:
        return [""]

    if len(text) <= max_chars:
        return [text]

    chunks = []
    start = 0
    text_len = len(text)

    while start < text_len:
        end = min(start + max_chars, text_len)

        if end >= text_len:
            chunks.append(text[start:end])
            break

        split_pos = max(
            text.rfind("\n\n", start, end),
            text.rfind("\n", start, end),
            text.rfind(" ", start, end),
        )

        if split_pos <= start + max_chars // 2:
            split_end = end
        else:
            if text.startswith("\n\n", split_pos):
                split_end = split_pos + 2
            else:
                split_end = split_pos + 1

        chunks.append(text[start:split_end])
        start = split_end

    return chunks


